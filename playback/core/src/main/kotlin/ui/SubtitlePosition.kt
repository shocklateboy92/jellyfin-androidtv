package org.jellyfin.playback.core.ui

/**
 * Represents the position where subtitles should be displayed.
 */
enum class SubtitlePosition {
	/**
	 * Primary subtitles displayed at the bottom of the screen (traditional position).
	 */
	PRIMARY,
	
	/**
	 * Secondary subtitles displayed at the top of the screen.
	 */
	SECONDARY
}